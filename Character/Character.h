#pragma once
#include <iostream>
#include <string>

class Character
{
    
public:
    //================================================================================================
    //=============================== PROPERTIES & VARIABLES =========================================
    //================================================================================================
    int Health = 100;
    int AttackPower = 10;
    int MagicPower = 10;
    int Mana = 50;
    int Gold = 0;
    std::string Name = "Unidentified";
    bool bIsDead = false;
    int ClassID = 0;


    //================================================================================================
    //====================================== FUNCTIONS ===============================================
    //================================================================================================
    virtual bool Attack(Character& target) = 0;
    virtual void Heal() = 0;
    virtual void Status();
    virtual void RecoverMana();
    virtual void TakeDamage(int damage);


protected:
    //================================================================================================
    //=============================== PROPERTIES & VARIABLES =========================================
    //================================================================================================
 

 
    //================================================================================================
    //====================================== FUNCTIONS ===============================================
    //================================================================================================
    virtual void Die();
    virtual int CalculateDamage(float multiplier, int damagetype);
    void TypeText(const std::string& text);

};
